export function getImageUrl(imageId, width, format = 'webp') {
  return `https://images.unsplash.com/photo-${imageId}?fit=crop&w=${width}&q=45&fm=${format}`
}

export function getImageSrcset(imageId, format = 'webp') {
  return [400, 700, 900]
    .map((w) => `${getImageUrl(imageId, w, format)} ${w}w`)
    .join(', ')
}

